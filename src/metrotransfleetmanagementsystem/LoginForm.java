    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String username = jTextField1.getText();
    String password = new String(jPasswordField1.getPassword());

    // Perform user authentication
    if (authenticateUser(username, password)) {
        String userRole = getUserRole(username);

        if (null == userRole) {
            JOptionPane.showMessageDialog(this, "Login successful. Your role is: " + userRole);
        } else // Check the user's role and navigate to the respective dashboard
           switch (userRole) {
               case "Admin" -> {
                   // Open the Admin Dashboard
                   AdminDashboard adminDashboard = new AdminDashboard();
                   adminDashboard.setVisible(true);
            }
               case "Dispatcher" -> {
                   // Open the User Dashboard
                   DispatchDashboard dispatchDashboard = new DispatchDashboard();
                   dispatchDashboard.setVisible(true);
            }
               default -> JOptionPane.showMessageDialog(this, "Login successful.");
           }
    } else {
        JOptionPane.showMessageDialog(this, "Login failed. Please check your credentials.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed
   private boolean authenticateUser(String username, String password) {
    try {
        Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
        String query = "SELECT UserID FROM Users WHERE UserName = ? AND UserPassword = ?";
        PreparedStatement preparedStatement = connection.prepareStatement(query);
        preparedStatement.setString(1, username);
        preparedStatement.setString(2, password);
        ResultSet resultSet = preparedStatement.executeQuery();

        return resultSet.next(); // Check if any rows are returned
    } catch (SQLException e) {
        e.printStackTrace();
        return false;
    }
}

private String getUserRole(String username) {
    try {
        Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
        String query = "SELECT UserRole FROM UserRoles WHERE UserID = (SELECT UserID FROM Users WHERE UserName = ?)";
        PreparedStatement preparedStatement = connection.prepareStatement(query);
        preparedStatement.setString(1, username);
        ResultSet resultSet = preparedStatement.executeQuery();

        if (resultSet.next()) {
            return resultSet.getString("UserRole");
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    return "Unknown";
}

